"""Command-line interface for the dedupe toolkit."""

from __future__ import annotations

import argparse
import csv
import logging
import sqlite3
import sys
from pathlib import Path
from typing import Iterable, Optional, Set

from . import manifest, matcher, rstudio_parser, scanner

# Ensure we load the local `dedupe` package when running from the repo tree.
sys.path.insert(0, str(Path(__file__).resolve().parent.parent))

LOGGER = logging.getLogger(__name__)


def _configure_logging(verbose: bool) -> None:
    level = logging.DEBUG if verbose else logging.INFO
    logging.basicConfig(
        level=level,
        format="%(asctime)s %(levelname)s %(name)s: %(message)s",
    )


def _path(value: str) -> Path:
    return Path(value).expanduser()


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        prog="dedupe",
        description="Audio recovery toolkit",
    )
    parser.add_argument(
        "--verbose",
        action="store_true",
        help="Enable verbose logging output",
    )
    subparsers = parser.add_subparsers(dest="command", required=True)

    # ------------------------------------------------------------------
    # scan-library
    # ------------------------------------------------------------------
    scan_parser = subparsers.add_parser(
        "scan-library",
        help="Scan an audio library and write metadata to SQLite",
    )
    scan_parser.add_argument(
        "--root",
        type=_path,
        required=True,
        help="Root directory to scan",
    )
    scan_parser.add_argument(
        "--out",
        type=_path,
        required=True,
        help="SQLite database path to populate",
    )
    scan_parser.add_argument(
        "--fingerprints",
        action="store_true",
        help=(
            "Capture optional Chromaprint fingerprints (requires fpcalc; "
            "skipped automatically when unavailable)"
        ),
    )
    scan_parser.add_argument(
        "--resume",
        action="store_true",
        help=(
            "Resume a previous scan by skipping unchanged files present in "
            "the database (size + mtime check)"
        ),
    )
    scan_parser.add_argument(
        "--progress",
        action="store_true",
        help="Show a progress bar during scanning",
    )
    scan_parser.add_argument(
        "--workers",
        type=int,
        default=1,
        help="Number of parallel workers to use (default: 1)",
    )

    # ------------------------------------------------------------------
    # parse-rstudio
    # ------------------------------------------------------------------
    parse_parser = subparsers.add_parser(
        "parse-rstudio",
        help="Parse an R-Studio Recognized Files export into SQLite",
    )
    parse_parser.add_argument(
        "--input",
        type=_path,
        required=True,
        help="R-Studio export path",
    )
    parse_parser.add_argument(
        "--out",
        type=_path,
        required=True,
        help="SQLite database to store recovery candidates",
    )

    # ------------------------------------------------------------------
    # match
    # ------------------------------------------------------------------
    match_parser = subparsers.add_parser(
        "match",
        help="Match scanned library metadata with recovery candidates",
    )
    match_parser.add_argument(
        "--library",
        type=_path,
        required=True,
        help="Library SQLite database",
    )
    match_parser.add_argument(
        "--recovered",
        type=_path,
        required=True,
        help="Recovered files SQLite database",
    )
    match_parser.add_argument(
        "--out",
        type=_path,
        required=True,
        help="CSV file to write match results",
    )

    # ------------------------------------------------------------------
    # generate-manifest
    # ------------------------------------------------------------------
    manifest_parser = subparsers.add_parser(
        "generate-manifest",
        help="Create a recovery manifest from match results",
    )
    manifest_parser.add_argument(
        "--matches",
        type=_path,
        required=True,
        help="CSV file produced by the match command",
    )
    manifest_parser.add_argument(
        "--out",
        type=_path,
        required=True,
        help="Destination CSV for the recovery manifest",
    )

    # ------------------------------------------------------------------
    # verify-manifest
    # ------------------------------------------------------------------
    verify_parser = subparsers.add_parser(
        "verify-manifest",
        help=(
            "Validate a manifest against library.db and recovered.db. "
            "Lists all problems (missing paths, duplicates, etc.)."
        ),
    )
    verify_parser.add_argument(
        "manifest",
        type=_path,
        help="Path to manifest CSV (generated by generate-manifest)",
    )
    verify_parser.add_argument(
        "--library-db",
        type=_path,
        default=Path("artifacts/db/library.db"),
        help="Path to library SQLite DB (default: artifacts/db/library.db)",
    )
    verify_parser.add_argument(
        "--recovered-db",
        type=_path,
        default=Path("artifacts/db/recovered.db"),
        help="Path to recovered SQLite DB (default: artifacts/db/recovered.db)",
    )

    return parser


def _command_scan(args: argparse.Namespace) -> int:
    total = scanner.scan(
        root=args.root,
        database=args.out,
        include_fingerprints=args.fingerprints,
        resume=getattr(args, "resume", False),
        show_progress=getattr(args, "progress", False),
        workers=getattr(args, "workers", 1),
    )
    LOGGER.info("Indexed %s files", total)
    return 0


def _command_parse_rstudio(args: argparse.Namespace) -> int:
    records = list(rstudio_parser.parse_export(args.input))
    count = rstudio_parser.load_into_database(records, args.out)
    LOGGER.info("Captured %s recovery candidates", count)
    return 0


def _command_match(args: argparse.Namespace) -> int:
    matcher.match_databases(args.library, args.recovered, args.out)
    return 0


def _command_manifest(args: argparse.Namespace) -> int:
    manifest.generate_manifest(args.matches, args.out)
    return 0


